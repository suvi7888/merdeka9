                        5.  Tahun 1961 : Pemerintah membentuk Biro Minyak dan Gas Bumi yang berada dibawah Departemen Perindustrian Dasar dan Pertambangan.
                        6.  Tahun 1962 : Jawatan Geologi dan Jawatan Pertambangan diubah menjadi Direktorat Geologi dan Direktorat Pertambangan.
                        7.  Tahun 1963 : Biro Minyak dan Gas Bumi diubah menjadi Direktorat Minyak dan Gas Bumi yang berada dibawah kewenangan Pembantu Menteri Urusan Pertambangan dan Perusahaan-perusahaan Tambang Negara.
                        8.  Tahun 1965 : Departemen Perindustrian Dasar/Pertambangan dipecah menjadi tiga departemen yaitu : Departemen Perindustrian Dasar, Departemen Pertambangan dan Departemen Urusan Minyak dan Gas Bumi. 11 Juni 1965: Menteri Urusan Minyak dan Gas Bumi menetapkan berdirinya Lembaga Minyak dan Gas Bumi (Lemigas).
                        9.  Tahun 1966 : Departemen Urusan Minyak dan Gas Bumi dilebur menjadi Kementerian Pertambangan dan Migas yang membawahi Departemen Minyak dan Gas Bumi.
                        10.     Tahun 1966 : Departemen Minyak dan Gas Bumi dan Departemen Pertambangan dilebur menjadi Departemen Pertambangan.
                        11.     Tahun 1978 : Departemen Pertambangan berubah menjadi Departemen Pertambangan dan Energi.
                        12.     Tahun 2000 : Departemen Pertambangan dan Energi berubah menjadi Departemen Energi dan Sumber Daya Mineral.

