                        2.  Tahun 1952 : Pembentukan Direktorat Pertambangan yang terdiri atas Pusat Jawatan Pertambangan dan Pusat Jawatan Geologi, berdasarkan SK Menteri Perekonomian No. 2360a/M Tahun 1952.
